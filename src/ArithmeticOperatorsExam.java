public class ArithmeticOperatorsExam {
    public static void main (String args []){
        byte num1 = 100;
        byte num2 = 20;

        System.out.println("num1 + num2 = " + (num1+num2));
        System.out.println("num1 - num2 = " + (num1-num2));
        System.out.println("num1 / num2 = "+ (num1/num2));
        System.out.println("num1 * num2 = "+ (num1*num2));

    }
}
